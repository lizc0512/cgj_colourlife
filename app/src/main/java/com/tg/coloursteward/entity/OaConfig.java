package com.tg.coloursteward.entity;

public class OaConfig {


    /**
     * code : 0
     * message : 请求成功
     * content : {"number":2}
     */

    private int code;
    private String message;
    private ContentBean content;

    public boolean isSuccess() {
        return code == 0;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public ContentBean getContent() {
        return content;
    }

    public void setContent(ContentBean content) {
        this.content = content;
    }

    public static class ContentBean {
        /**
         * number : 2
         */

        private int number;
        private int waitingBalanceCount;

        public int getWaitingBalanceCount() {
            return waitingBalanceCount;
        }

        public void setWaitingBalanceCount(int waitingBalanceCount) {
            this.waitingBalanceCount = waitingBalanceCount;
        }

        public int getNumber() {
            return number;
        }

        public void setNumber(int number) {
            this.number = number;
        }
    }
}
